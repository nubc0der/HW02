class Calculator:

    def __init__(self):
        pass

    def addition(self, a, b):
        return a + b
    def subtraction(self, a, b):
        return a - b
    def multiplication(self, a, b):
        return a * b
    def division(self, a, b):
        return a / b
    def squareRoot(self, a):
        return a ** 0.5
    def square(self, a):
        return a * a